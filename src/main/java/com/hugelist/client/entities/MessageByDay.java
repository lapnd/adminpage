package com.hugelist.client.entities;

import java.util.Date;

public class MessageByDay implements java.io.Serializable {

	private Date date;
	private int sentNo;
	private int receivedNo;
	
	public MessageByDay(Date date, int sentNo, int receivedNo) {
		super();
		this.date = date;
		this.sentNo = sentNo;
		this.receivedNo = receivedNo;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public int getSentNo() {
		return sentNo;
	}
	public void setSentNo(int sentNo) {
		this.sentNo = sentNo;
	}
	public int getReceivedNo() {
		return receivedNo;
	}
	public void setReceivedNo(int receivedNo) {
		this.receivedNo = receivedNo;
	}

}
